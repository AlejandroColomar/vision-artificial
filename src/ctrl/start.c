/******************************************************************************
 ******* main *****************************************************************
 ******************************************************************************/
void	start_switch	(void)
{

	if (!saved_name[0])
		return;

	img_iface_init();
	errno	= 0;
	img_iface_load(NULL, saved_name);

	if (errno)
		goto err;

	user_iface_init();
	user_iface();
	user_iface_cleanup();
err:
	perrorx(saved_name);
	img_iface_cleanup();
}


