int	menu_iface_mode;


/******************************************************************************
 ******* main *****************************************************************
 ******************************************************************************/
void	menu_iface	(void)
{
	switch (menu_iface_mode) {
	case MENU_IFACE_FOO:
		break;

	case MENU_IFACE_CLUI:
		menu_clui();
		break;

	case MENU_IFACE_TUI:
		menu_tui();
		break;
	}
}


