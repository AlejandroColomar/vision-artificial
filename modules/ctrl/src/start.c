void	start_switch	(void)
{
	struct _IplImage	*imgptr;

	errno	= 0;
	imgptr	= img_iface_load();

	if (!errno) {
		user_iface_init();
		user_iface(imgptr);
		user_iface_cleanup();
	}
}


